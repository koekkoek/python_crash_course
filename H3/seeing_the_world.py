places = ["Praag", "Tenerife", "Groningen", "Bern", "Gardameer"]
print(places)

# sorting places just once
print(sorted(places))
print(places)

# sorting places just once in reverse order
print(sorted(places, reverse=True))
print(places)

# reverse list
places.reverse()
print(places)

# reverse list again
places.reverse()
print(places)

# sort in alphabetical order
places.sort()
print(places)

# sort in reverse-alphabetical order
places.sort(reverse=True)
print(places)